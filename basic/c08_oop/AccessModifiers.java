public class AccessModifiers {

    public static void main(String[] args) {

        // Encapsulamiento

        // - Modificadores de acceso
        // public
        // - Permite el acceso desde cualquier parte del programa.
        // - Se utiliza para clases, métodos y atributos que deben ser accesibles globalmente.
        // private
        // - Restringe el acceso solo a la propia clase.
        // - Se utiliza para ocultar detalles de implementación y proteger la integridad de los datos
        // protected
        // - Permite el acceso a la propia clase, a subclases y a clases del mismo paquete.
        // - Se utiliza para atributos y métodos que deben ser accesibles por herencia, pero no por otras clases.
        // (default)
        // - Si no se especifica un modificador, el acceso es por defecto (package-private).
        // - Permite el acceso solo a clases del mismo paquete.
        // getters
        // - Métodos que permiten acceder a los atributos privados de una clase.
        // - Se utilizan para obtener el valor de un atributo sin permitir su modificación directa.
        // setters
        // - Métodos que permiten modificar los atributos privados de una clase.
        // - Se utilizan para establecer el valor de un atributo, a menudo con validaciones.

    }
}
